
#include "ip_address.h"


//////////////////////////////////////////////////////////////////////
//  CONSTRUCTOR
//////////////////////////////////////////////////////////////////////
IpAddress::IpAddress( )
{
}


//////////////////////////////////////////////////////////////////////
//  CONSTRUCTOR
//////////////////////////////////////////////////////////////////////
IpAddress::IpAddress( const std::string &ipAddress_, const int &prefixLen_ )
  : m_Address( ipAddress_ )
  , m_PrefixLength( prefixLen_ )
{
}


//////////////////////////////////////////////////////////////////////
// DESTRUCTOR
//////////////////////////////////////////////////////////////////////
IpAddress::~IpAddress( )
{
}

std::string IpAddress::GetAddress( ) const
{
  return m_Address;
}


int IpAddress::GetPrefixLength( ) const
{
  return m_PrefixLength;
}
